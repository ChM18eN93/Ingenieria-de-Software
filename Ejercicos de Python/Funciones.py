# Funión Mostrar doble con un parámetro
def mostrar_doble(num):
    print(num * 2)

# Llamada a la función
mostrar_doble(2)

# Función suma con 2 parámetros
def sumar(x, y):
    print(x + y)

# Llamada a la función
sumar(3, 2)


# Función con parámetros y return

# Función suma con 2 parámetros
def sumar(x, y):
    return x + y
resultado = sumar(4, 5)

